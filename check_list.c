#include "push_swap.h"

int	check_list(char *lst)
{
	int	i;

	i = 0;
	while (lst[i])
	{
		if (i == 0 && (lst[i] == '-' || lst[i] == '+'))
			i++;
		if ((lst[i] > '9' || lst[i] < '0'))
			return (1);
		i++;
	}
	return (0);
}
